Long = {'Bề ngoài' : 'Bình thường', 'Học vấn' : 'Bỏ ngang đại học', 'Tài chính' : 'Éo có gì'} # đây là dictionary (từ điển), sử dụng {} thay vì []
print(Long['Bề ngoài']) # 'bề ngoài' được gọi là key còn 'bình thường' là value, cả 2 cái gọi là key pair
# từ điển không có thứ tự, chỉ cần giống nhau về key pair thì kết quả trả về là True
# từ điển sử dụng bản sao (reference) như list